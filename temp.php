<h2 class="wp-block-heading">Eligibility</h2>
<!-- /wp:heading -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading">Continuing and Term Employees</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Full and part-time continuing employees and full and part-time term employees with appointments of 12 months or more are eligible to join the plan.</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
  <li>You may be enrolled in the plan on the first day of any month coincident with or following your date of employment.</li>
  <!-- /wp:list-item -->

  <!-- wp:list-item -->
  <li>Membership is compulsory by the first day of July after you reach age 30.</li>
  <!-- /wp:list-item -->
</ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>If you are appointed to less than a one-year term, you are not eligible to join the plan. &nbsp;If your term is extended beyond a one-year period, you may join the plan retroactive to the date of your appointment.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Staff with full-time term appointments of at least six months but less than 12 months, who have been appointed to a second term, will be eligible to join the plan from the date of appointment to the second term if the break between the first and second term is less than 12 months.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p><strong>Note:</strong> Once you join the plan, you must remain a member while employed by the University.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Contact <a href="https://carleton.ca/hr/contact-us/#pension">Pension Services</a> if you have any questions regarding the information on these pages. Contact the Office of&nbsp;<a href="http://carleton.ca/finance-admin/pension-fund-management/">Pension Fund Management</a>&nbsp;if you have questions regarding the plan governance or investments.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading">Part-Time Employees</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Eligible part-time employees are those who meet one of the two requirements below in each of the previous two calendar years:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
  <li>Earn at least 35% of the&nbsp;<a href="https://carleton.ca/hr/payroll/your-pay-information/statutory-deductions/">CPP Yearly Maximum Pensionable Earnings (YMPE)</a> under the Canada Pension Plan (CPP) or</li>
  <!-- /wp:list-item -->

  <!-- wp:list-item -->
  <li>Work 700 hours or more</li>
  <!-- /wp:list-item -->
</ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>Once a part-time employee becomes a member of the Plan, membership will continue if employment continues, regardless of earnings level or hours worked.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p><strong>Note:</strong> Once you join the plan, you must remain a member while employed by the University.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Contact <a href="https://carleton.ca/hr/contact-us/#pension">Pension Services</a> if you have any questions regarding the information on these pages. Contact the Office of&nbsp;<a href="http://carleton.ca/finance-admin/pension-fund-management/">Pension Fund Management</a>&nbsp;if you have questions regarding the plan governance or investments.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">Growing Your Pension Assets</h2>
<!-- /wp:heading -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading">Employee Contributions</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>You are required to contribute 4.37% of your earnings up to the <a href="https://carleton.ca/hr/payroll/your-pay-information/statutory-deductions/">CPP Yearly Maximum Pensionable Earnings (YMPE)</a> plus 6% of your earnings above the YMPE. Your required contributions are credited to an account set up on your behalf, called the Money Purchase Component Account (MPC).</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Contributions are tax-deductible.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p><strong>Example:</strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>If the YMPE is $66,600 (2023) and you earn $80,000 per annum, the annual contribution total is $3,714.42. The calculation is as follows:</p>
<!-- /wp:paragraph -->

<!-- wp:table -->
<figure class="wp-block-table">
  <table class="has-fixed-layout">
    <thead>
      <tr>
        <th><strong>Money Purchase Component (MPC) Contribution</strong></th>
        <th></th>
      </tr>
    </thead>
    <tbody>
      <tr>
        <td>4.37% of $66,600</td>
        <td>$2,910.42</td>
      </tr>
      <tr>
        <td>6% of ($80,000 - $66,600)</td>
        <td>$804.00</td>
      </tr>
