<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading">Employer Contributions</h3>
<!-- /wp:heading -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
  <li>The employer contributes an amount equal to your MPC contributions plus 0.25% of your pensionable earnings to your Money Purchase Component Account.</li>
  <!-- /wp:list-item -->

  <!-- wp:list-item -->
  <li>The employer also contributes, as required, the normal cost of the Minimum Guarantee Pension and the amortization of any unfunded liability or solvency deficit.<a id="avc"></a></li>
  <!-- /wp:list-item -->
</ul>
<!-- /wp:list -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading">Additional Voluntary Contributions (AVC)</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>You may elect to make additional voluntary contributions (AVCs) to the Carleton University Retirement Plan for the purpose of increasing your pension benefits.&nbsp; At retirement, &nbsp;Additional Voluntary Contributions can be withdrawn in cash, transferred to an RRSP (Registered Retirement Savings Plan) or RRIF, or left in the Plan. However, any special transferred contributions that were subject to “locking in” must remain locked in. Cash withdrawals are subject to taxation.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Note that you may make a one-time withdrawal of your AVC balance from the Plan prior to your termination of employment or retirement. After electing to do so, you will not be eligible to make any further AVCs to the plan.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Your contribution may take the form of an RRSP transfer, a lump sum deposit, or a payroll deduction.&nbsp; Voluntary cash contributions or payroll deductions are tax deductible.</p>
<!-- /wp:paragraph -->

