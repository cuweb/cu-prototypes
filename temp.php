<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading">Buying Back Past Service</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>If you have continuous service with the University during which you were eligible to participate in the Plan, but did not (example: If you did not join the retirement plan prior to age 30), you may purchase all or part of your eligible past service subject to Income Tax Act limits.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The actual cost will depend on your salary and age at the time the service is purchased.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>A <a href="https://carleton.ca/hr/contact-us/#pension">Pension Specialist</a> can advise you whether or not you are eligible to make past service contributions. Past service contributions are credited to your Defined Benefit Contribution Account and increase your pension at retirement. These contributions are tax deductible and subject to Income Tax Act limits.</p>
<!-- /wp:paragraph -->

