<h3 class="wp-block-heading">Pension Portability</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>You may transfer any pension contributions made by you and by your previous employer to the University pension plan. Transfers apply either where the University has a reciprocal agreement with your previous employer or under the portability provisions of the Carleton University Retirement Plan.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>If you do transfer contributions, you may request, within twelve months of becoming a member, that your period of credited service be increased.</p>
